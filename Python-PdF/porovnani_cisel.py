while True:
    cislo1=int(input("Zadejte první číslo:"))
    cislo2=int(input("Zadejte druhé číslo:"))
    if cislo1>cislo2:
        print("\nPrvní číslo je větší než číslo druhé")
    elif cislo1==cislo2:
        print("\nPrvní číslo je rovno číslu druhému")    
    else:
            print("\nDruhé číslo je větší než číslo první")